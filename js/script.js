// genero i numeri da 1 a 100
for (var i = 1; i <= 100; i++) {
  // per i multipli di 3 stampo Fizz
  if (i % 3 == 0) {
    console.log("Fizz");
  }
  // per i multipli di 5 stampo Buzz
  else if (i % 5 == 0) {
    console.log("Buzz");
  }

  else  {
    console.log(i);
  }
}





